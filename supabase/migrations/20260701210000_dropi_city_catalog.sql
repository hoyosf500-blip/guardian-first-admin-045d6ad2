-- Catalogo de ciudades Dropi (cod_dane) por pais, para resolver la ciudad destino
-- SIN llamar /api/locations (bloqueado por IP de datacenter desde el edge de Supabase).
-- Poblado desde el catalogo web de Dropi (DROPI_cities), pais EC. 410 ciudades.

CREATE TABLE IF NOT EXISTS public.dropi_city_catalog (
  id            bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  country_code  text NOT NULL,
  city_norm     text NOT NULL,
  dept_norm     text NOT NULL DEFAULT '',
  city_id       integer NOT NULL,
  name          text NOT NULL,
  department_id integer,
  cod_dane      text NOT NULL,
  created_at    timestamptz NOT NULL DEFAULT now()
);

CREATE UNIQUE INDEX IF NOT EXISTS dropi_city_catalog_lookup
  ON public.dropi_city_catalog (country_code, city_norm, dept_norm);
CREATE INDEX IF NOT EXISTS dropi_city_catalog_city
  ON public.dropi_city_catalog (country_code, city_norm);

ALTER TABLE public.dropi_city_catalog ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS dropi_city_catalog_read ON public.dropi_city_catalog;
CREATE POLICY dropi_city_catalog_read ON public.dropi_city_catalog
  FOR SELECT TO authenticated USING (true);

